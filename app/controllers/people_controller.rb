class PeopleController < ApplicationController
  before_action :set_person, only: %i[ show edit update destroy ]

  # GET /people or /people.json
  def index
    @people = Person.all
  end

  # GET /people/1 or /people/1.json
  def show
  end

  # GET /people/new
  def new
    @membership = Membership.find(params[:membership_id])
    @person = @membership.people.build(last_name: @membership.last_name)
  end

  # GET /people/1/edit
  def edit
  end

  # POST /people or /people.json
  def create
    @membership = Membership.find(params[:membership_id])
    @person = @membership.people.build(person_params)

    respond_to do |format|
      if @person.save
        # if params[:add_another]
        #   format.html { redirect_to new_person_path(membership_id: @person.membership_id), notice: "Person was successfully added. Add Another." }
        # else
        #   format.html { redirect_to membership_url(@person.membership_id), notice: "Person was successfully added." }
        # end
        format.html { redirect_to membership_url(@person.membership_id), notice: "Person was successfully added." }
        format.json { render :show, status: :created, location: @person }
      else
        format.html { render :new, status: :unprocessable_entity }
        format.json { render json: @person.errors, status: :unprocessable_entity }
      end
    end
  end

  # PATCH/PUT /people/1 or /people/1.json
  def update
    respond_to do |format|
      if @person.update(person_params)
        format.html { redirect_to membership_url(@person.membership_id), notice: "Person was successfully updated." }
        format.json { render :show, status: :ok, location: @person }
      else
        format.html { render :edit, status: :unprocessable_entity }
        format.json { render json: @person.errors, status: :unprocessable_entity }
      end
    end
  end

  # DELETE /people/1 or /people/1.json
  def destroy
    @person.destroy!

    respond_to do |format|
      format.html { redirect_to membership_url(@person.membership_id), notice: "Person was successfully deleted." }
      format.json { head :no_content }
    end
  end

  private
    # Use callbacks to share common setup or constraints between actions.
    def set_person
      @person = Person.find(params[:id])
    end

    # Only allow a list of trusted parameters through.
    def person_params
      params.require(:person).permit(:membership_id, :first_name, :last_name, :dob, :birth_year)
    end
end
